    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
        // TODO add your handling code here:
        String orderType, mealCategory, mealType="", hotOrcold="";
        int couponId, discount, serve;
        boolean alcholic=false, prepared=false, diabetic=false;
        double price;
        
        
        serve=Integer.parseInt(serve_TF.getText());
        price=Double.parseDouble(price_TF.getText());
        
        String str=orderType_CB.getSelectedItem().toString();
        if(str.equalsIgnoreCase("meal")){
            orderType="meal";
            mealCategory=category_CB.getSelectedItem().toString();
            if(vegetarian_RB.isSelected())
                mealType="vegetarian";
            if(meat_RB.isSelected())
                mealType="meat";
            if(chicken_RB.isSelected())
                mealType="chicken";
            
            Meal m=new Meal(serve,price, orderType, mealCategory,mealType);
            OrderSYS.addOrder(m);
            if(checkCoupon()!=null)
            {
                Coupon c=checkCoupon();
                m.addCoupon(c);
                m.setPrice(price-(price*c.getDiscount()/100));
            }
            
            

            
        }
        else if(str.equalsIgnoreCase("drink")){
            orderType="drink";
            if(hot_RB.isSelected())
                hotOrcold="hot";
            if(cold_RB.isSelected())
                hotOrcold="cold";
            if(alcholic_CB.isSelected())
                alcholic=true;
            Drink d=new Drink(serve, price, orderType, hotOrcold, alcholic);
            OrderSYS.addOrder(d);
            if(checkCoupon()!=null)
            {
                Coupon c=checkCoupon();
                d.addCoupon(c);
                d.setPrice(price-(price*c.getDiscount()/100));
            }
        }
        
        else if(str.equalsIgnoreCase("dessert")){
            orderType="dessert";
            if(diabetic_CB.isSelected())
                diabetic=true;
            if(prepared_CB.isSelected())
                prepared=true;
            
            Dessert dt=new Dessert(serve, price, orderType, prepared, diabetic);
            OrderSYS.addOrder(dt);
            
            if(checkCoupon()!=null)
            {
                Coupon c=checkCoupon();
                dt.addCoupon(c);
                dt.setPrice(price-(price*c.getDiscount()/100));
            }
            
           
        }
        
        
        
    }//GEN-LAST:event_add_buttonActionPerformed

