    private Coupon checkCoupon(){
        if(coupon_panel.isVisible()){
            int couponId=Integer.parseInt(couponId_TF.getText());
            int discount=Coupon.getDiscountRate(couponId);
            double price=Double.parseDouble(price_TF.getText());
            if(discount!=0){
                Coupon c=new Coupon(couponId, discount);
                msg_label.setText("Discount Rate: "+discount+"%\n New Price: "+(price-(price*c.getDiscount()/100)));
                return c;                
            }
            else{
                msg_label.setText("No coupon found!");                
            }
        }
        return null;
    }
    
    
